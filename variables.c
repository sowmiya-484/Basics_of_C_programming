#include <stdio.h>

int main()
{
    int age = 20;           
    float salary = 25000.5; 
    char grade = 'A';       
    double pi = 3.141592;   

    printf("Age = %d\n", age);
    printf("Salary = %.2f\n", salary);
    printf("Grade = %c\n", grade);
    printf("Pi = %lf\n", pi);

    return 0;
}
