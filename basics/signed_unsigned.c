#include<stdio.h>
int main()
{
	signed int a=10;
	signed int b=-19;
	unsigned int c=10;
	unsigned int d=-19;
	printf("the signed int with positive value %d \n",a);
	printf("the signed int with negative value %d \n",b);
	printf("the unsigned int with positive value %u \n",c);
	printf("the unsigned int with negative value %u \n",d);
	return 0;
}
